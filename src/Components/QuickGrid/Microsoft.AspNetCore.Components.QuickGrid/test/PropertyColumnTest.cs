    [Fact]
    public void PropertyColumn_SortBy_IsSetFromPropertyExpression()
    {
        // Arrange
        var column = new PropertyColumn<TestEntity, string>();
        Expression<Func<TestEntity, string>> propertyExpr = x => x.Name;

        // Act - set Property through reflection to simulate component lifecycle
        var propertyInfo = typeof(PropertyColumn<TestEntity, string>).GetProperty("Property");
        propertyInfo?.SetValue(column, propertyExpr);

        // Use reflection to call protected OnParametersSet
        var onParamsMethod = typeof(PropertyColumn<TestEntity, string>).GetMethod("OnParametersSet", BindingFlags.NonPublic | BindingFlags.Instance);
        onParamsMethod?.Invoke(column, null);

        // Assert - SortBy should be automatically created from the property
        Assert.NotNull(column.SortBy);
    }

