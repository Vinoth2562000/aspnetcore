        var isSortableByDefaultMethod = typeof(PropertyColumn<TestEntity, string>).BaseType.GetMethod("IsSortableByDefault", BindingFlags.NonPublic | BindingFlags.Instance);
        var isSortableByDefault = isSortableByDefaultMethod?.Invoke(column, null);
        Assert.False((bool)isSortableByDefault!); // Base class returns false
    }
