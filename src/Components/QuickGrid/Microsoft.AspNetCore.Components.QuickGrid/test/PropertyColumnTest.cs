    [Fact]
    public void PropertyColumn_FormatNonIFormattable_ThrowsInvalidOperation()
    {
        // Arrange
        var column = new PropertyColumn<TestEntity, TestEntity>();
        Expression<Func<TestEntity, TestEntity>> propertyExpr = x => x;

        // Act
        var propertyInfo = typeof(PropertyColumn<TestEntity, TestEntity>).GetProperty("Property");
        propertyInfo?.SetValue(column, propertyExpr);
        column.Format = "someformat";

        var onParamsMethod = typeof(PropertyColumn<TestEntity, TestEntity>).GetMethod("OnParametersSet", BindingFlags.NonPublic | BindingFlags.Instance);

        // Assert - reflection wraps exception, so we check inner exception
        var exception = Assert.Throws<TargetInvocationException>(() => onParamsMethod?.Invoke(column, null));
        Assert.IsType<InvalidOperationException>(exception.InnerException);
        Assert.Contains("does not implement", exception.InnerException!.Message);
    }

